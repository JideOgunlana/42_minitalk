#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*current;

	current = lst;
	if (current == NULL)
		return ;
	while (current != NULL)
	{
		f(current->content);
		current = current->next;
	}
}
